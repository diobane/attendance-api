-- 1. TABELA DE FAMÍLIA (ÂNCORA)
CREATE TABLE "TB_FAMILY" (
    FAMILY_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CREATED_AT TIMESTAMPTZ DEFAULT NOW()
);

-- 2. TABELA DE ENDEREÇOS
CREATE TABLE "TB_ADDRESS" (
    ADDRESS_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHURCH_NAME VARCHAR(100),
    COUNTRY VARCHAR(20),
    CITY VARCHAR(20),
    STREET VARCHAR(100),
    NUMBER TEXT,
    COMPLEMENT VARCHAR(200)
);

-- 3. TABELA DE CONTATOS
CREATE TABLE "TB_CONTACT" (
    CONTACT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PHONE_1 VARCHAR(20),
    PHONE_2 VARCHAR(20),
    EMAIL VARCHAR(100)
);

-- 4. TABELA DE EQUIPES/TIMES
CREATE TABLE "TB_TEAM" (
    TEAM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(100),
    COLOR VARCHAR(20)
);

-- 5. TABELA DE EVENTOS
CREATE TABLE "TB_EVENT" (
    EVENT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(100),
    ADDRESS_ID BIGINT REFERENCES "TB_ADDRESS"(ADDRESS_ID),
    START_DATE TIMESTAMPTZ,
    END_DATE TIMESTAMPTZ
);

-- 6. TABELA DE CRIANÇAS
CREATE TABLE "TB_CHILD" (
    CHILD_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(100) NOT NULL,
    TEAM_ID BIGINT REFERENCES "TB_TEAM"(TEAM_ID),
    FAMILY_ID BIGINT REFERENCES "TB_FAMILY"(FAMILY_ID) ON DELETE CASCADE,
    RELIGION VARCHAR(100),
    FROM_EVENT_CHURCH BOOLEAN DEFAULT FALSE,
    OBSERVATION TEXT,
    CREATED_AT TIMESTAMPTZ DEFAULT NOW()
);

-- 7. TABELA DE RESPONSÁVEIS
CREATE TABLE "TB_RESPONSIBLE" (
    RESPONSIBLE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(100) NOT NULL,
    FAMILY_ID BIGINT REFERENCES "TB_FAMILY"(FAMILY_ID) ON DELETE CASCADE,
    CONTACT_ID BIGINT REFERENCES "TB_CONTACT"(CONTACT_ID),
    CREATED_AT TIMESTAMPTZ DEFAULT NOW()
);

-- 8. TABELA DE PRESENÇA (ATTENDANCE)
CREATE TABLE "TB_ATTENDANCE" (
    ATTENDANCE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHILD_ID BIGINT REFERENCES "TB_CHILD"(CHILD_ID) ON DELETE CASCADE,
    CREATED_AT TIMESTAMPTZ DEFAULT NOW(),
    TYPE CHAR(1) NOT NULL,
    CONSTRAINT CHECK_ATTENDANCE_TYPE CHECK (TYPE IN ('1', '2'))
);

-- COMENTÁRIOS DE REFERÊNCIA
COMMENT ON COLUMN "TB_ATTENDANCE".TYPE IS '1 - Checkin, 2 - Checkout';



